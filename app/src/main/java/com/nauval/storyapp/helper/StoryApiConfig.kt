package com.nauval.storyapp.helper

import com.nauval.storyapp.BuildConfig
import com.nauval.storyapp.api.StoryApiService
import com.nauval.storyapp.api.StoryBusinessApiService
import com.nauval.storyapp.api.StoryPagingApiService
import com.nauval.storyapp.api.UserApiService
import okhttp3.OkHttpClient
import okhttp3.logging.HttpLoggingInterceptor
import retrofit2.Retrofit
import retrofit2.converter.gson.GsonConverterFactory

class StoryApiConfig {
    companion object{
        fun getUserApiService(): UserApiService {
            val interceptor = if(BuildConfig.DEBUG) {
                HttpLoggingInterceptor().setLevel(HttpLoggingInterceptor.Level.BODY)
            } else {
                HttpLoggingInterceptor().setLevel(HttpLoggingInterceptor.Level.NONE)
            }

            val client = OkHttpClient.Builder()
                .addInterceptor(interceptor)
                .build()
            val retrofit = Retrofit.Builder()
                .baseUrl(BASE_URL)
                .addConverterFactory(GsonConverterFactory.create())
                .client(client)
                .build()
            return retrofit.create(UserApiService::class.java)
        }

        fun getStoryApiService(): StoryApiService {
