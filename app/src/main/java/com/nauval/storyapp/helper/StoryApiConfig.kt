            }

            val client = OkHttpClient.Builder()
                .addInterceptor(interceptor)
                .build()
            val retrofit = Retrofit.Builder()
                .baseUrl(BASE_URL)
                .addConverterFactory(GsonConverterFactory.create())
                .client(client)
                .build()
            return retrofit.create(StoryPagingApiService::class.java)
        }

        fun getStoryBusinessApiService(): StoryBusinessApiService {
            val interceptor = if(BuildConfig.DEBUG) {
                HttpLoggingInterceptor().setLevel(HttpLoggingInterceptor.Level.BODY)
            } else {
                HttpLoggingInterceptor().setLevel(HttpLoggingInterceptor.Level.NONE)
            }

            val client = OkHttpClient.Builder()
                .addInterceptor(interceptor)
                .build()
            val retrofit = Retrofit.Builder()
                .baseUrl(BASE_URL)
                .addConverterFactory(GsonConverterFactory.create())
                .client(client)
                .build()
            return retrofit.create(StoryBusinessApiService::class.java)
        }

        var BASE_URL = BuildConfig.BASE_URL
        const val MIN_PASSWORD = 6
        const val STORY_EXTRA = "STORY_EXTRA"
        const val MIN_PAGE = 15
    }
}